# ══════════════════════════════════════════════════════════════
# BUILD PROMPT
# ══════════════════════════════════════════════════════════════
def build_prompt(
    question: str,
    context: str | None = None,
    fallback_context: str | None = None,
    memory_messages: list | None = None,
    emergency: bool = False,
    first_message: bool = True,
) -> ChatPromptTemplate:
    """
    ترتيب الـ messages:
        1. System prompt
        2. RAG / fallback context  (system — قبل السياق)
        3. Emergency override      (system — لو موجود)
        4. المحادثة السابقة       (human/assistant — محادثة حقيقية)
        5. السؤال الحالي          (human)

    الفرق الجوهري عن النسخة القديمة:
        قديم:
            ("system", "المستخدم: سؤال\nرفيق: رد")
            الموديل بيشوفها كـ "تعليمات نصية" — مش محادثة.
            فبيتجاهلها ويرد كأنه ما شافش اللي اتقال.

        دلوقتي:
            ("human", "سؤال")
            ("assistant", "رد")
            الموديل بيعرف إنها محادثة حقيقية جرت،
            ويبني عليها بشكل طبيعي تماماً.
    """
    messages: list[tuple[str, str]] = []

    # ── 1. System prompt ──────────────────────────────────
    system = SYSTEM_PROMPT
    if first_message:
        system += (
            "\n\n[تعليمة] دي أول رسالة — "
            "ابدأ بترحيب دافي وطبيعي في جملة واحدة بس، "
            "وبعدين انتقل للموضوع مباشرة."
        )
    messages.append(("system", system))

    # ── 2. RAG context ────────────────────────────────────
    if context and context.strip():
        messages.append((
            "system",
            "[معلومات طبية موثوقة — استخدمها لتعزيز ردك بدون نقل حرفي]\n"
            + context.strip()
        ))

    if fallback_context and fallback_context.strip():
        messages.append((
            "system",
            "[معلومة داعمة إضافية — استخدمها لو كانت مفيدة]\n"
            + fallback_context.strip()
        ))

    # ── 3. Emergency override ─────────────────────────────
    if emergency:
        messages.append((
            "system",
            "[تحذير] الأعراض دي ممكن تكون طارئة. "
            "وجّه المستخدم بهدوء وبوضوح للطوارئ فورًا."
        ))

    # ── 4. Conversation history as REAL chat turns ────────
    for msg in (memory_messages or []):
        if not isinstance(msg, dict):
            continue

        role    = str(msg.get("role",    "") or "").strip()
        content = str(msg.get("content", "") or "").strip()

        if not content:
            continue

        # ملخص المحادثة القديمة — نحطه كـ system مش assistant
        # عشان الموديل ميخلطوش برد حقيقي
        if role == "assistant" and content.startswith("ملخص المحادثة السابقة:"):
            messages.append((
                "system",
                "[ملخص ما سبق للسياق فقط]\n" + content
            ))
            continue

        if role == "user":
            messages.append(("human", content))
        elif role == "assistant":
            messages.append(("assistant", content))

    # ── 5. Current question ───────────────────────────────
    messages.append(("human", question.strip()))

    return ChatPromptTemplate.from_messages(messages)